export function isWalkable(x, y) {
    const tileX = Math.floor(x / TILE_SIZE)
    const tileY = Math.floor(y / TILE_SIZE)
    
    // Asegurarse de que las coordenadas están dentro de los límites del mapa
    if (tileX < 0 || tileX >= sharedMap[0].length || tileY < 0 || tileY >= sharedMap.length) {
      return false
    }
    
    // Devolver true si es FLOOR, false si es WALL
    return sharedMap[tileY][tileX] === FLOOR
  }
  
